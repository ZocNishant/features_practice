let showStory = document.querySelector("#storyboard");

let list = "";

arey.forEach(function (data, index) {
  list += `<div class="story">
  <img id="${index}" src="${data.pp}" alt="PP no ${index}" />
</div>`;
});

showStory.innerHTML = list;

showStory.addEventListener("click", function (details) {
  document.querySelector("#see_story").style.display = "block";

  document.querySelector("#see_story").style.backgroundImage = `url(${
    arey[details.target.id].story
  })`;

  setTimeout(function () {
    document.querySelector("#see_story").style.display = "none";
  }, 2000);
});
